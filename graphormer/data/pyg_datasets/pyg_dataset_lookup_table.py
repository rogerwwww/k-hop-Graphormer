class PYGDatasetLookupTable:
    @staticmethod
    def GetPYGDataset(dataset_spec: str, seed: int) -> Optional[Dataset]:
        split_result = dataset_spec.split(":")
        if len(split_result) == 2:
            name, params = split_result[0], split_result[1]
            params = params.split(",")
        elif len(split_result) == 1:
            name = dataset_spec
            params = []
        inner_dataset = None
        num_class = 1

        train_set = None
        valid_set = None
        test_set = None

        root = "dataset"
        with_resistance_distance = False
        with_airports = False
        if name == "qm7b":
            inner_dataset = MyQM7b(root=root)
        elif name == "qm9":
            inner_dataset = MyQM9(root=root)
        # elif name == "airports":
        #     with_airports = True
        #     inner_dataset = MyAirPorts(root=root, name=params[0])
        elif "zinc" in name:
            assert name in ["zinc-subset", "zinc-full", "zinc-rd-subset", "zinc-rd-full"]
            subset = name.split('-')[-1] == 'subset'
            with_resistance_distance = len(name.split('-')) == 3
            zinc_dataset_func = MyZINCRD if with_resistance_distance else MyZINC
            inner_dataset = zinc_dataset_func(root=root, subset=subset)
            train_set = zinc_dataset_func(root=root, split="train", subset=subset)
            valid_set = zinc_dataset_func(root=root, split="val", subset=subset)
            test_set = zinc_dataset_func(root=root, split="test", subset=subset)
        elif name == "moleculenet":
            nm = None
            for param in params:
                name, value = param.split("=")
                if name == "name":
                    nm = value
            inner_dataset = MyMoleculeNet(root=root, name=nm)
        else:
            raise ValueError(f"Unknown dataset name {name} for pyg source.")
        if train_set is not None:
            return GraphormerPYGDataset(
                    None,
                    seed,
                    None,
                    None,
                    None,
                    train_set,
                    valid_set,
                    test_set,
                    with_resistance_distance=with_resistance_distance,
                    with_airports=with_airports,
                )
        else:
            return (
                None
                if inner_dataset is None
                else GraphormerPYGDataset(inner_dataset, seed, with_resistance_distance=with_resistance_distance, with_airports=with_airports)
            )
